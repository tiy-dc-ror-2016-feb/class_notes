def print_contact(first_name: nil, last_name: nil, company: nil)
  p "-" * 73                                                      # => "-------------------------------------------------------------------------", "-------------------------------------------------------------------------", "-------------------------------------------------------------------------"
  p "#{first_name} #{last_name}" if first_name || last_name       # => "Russell Osborne", nil, "Mike West"
  p "(#{company})"                                                # => "(The Iron Yard)", "(The Iron Yard)", "(Umbrella Corp)"
  p "-" * 73                                                      # => "-------------------------------------------------------------------------", "-------------------------------------------------------------------------", "-------------------------------------------------------------------------"
end                                                               # => :print_contact

russell_hash = { first_name: "Russell", last_name: "Osborne", company: "The Iron Yard"}  # => {:first_name=>"Russell", :last_name=>"Osborne", :company=>"The Iron Yard"}

print_contact(russell_hash)  # => "-------------------------------------------------------------------------"

print_contact(company: "The Iron Yard")  # => "-------------------------------------------------------------------------"

print_contact(first_name: "Mike", last_name: "West", company: "OsCorp")  # => "-------------------------------------------------------------------------"

# >> "-------------------------------------------------------------------------"
# >> "Russell Osborne"
# >> "(The Iron Yard)"
# >> "-------------------------------------------------------------------------"
# >> "-------------------------------------------------------------------------"
# >> "(The Iron Yard)"
# >> "-------------------------------------------------------------------------"
# >> "-------------------------------------------------------------------------"
# >> "Mike West"
# >> "(Umbrella Corp)"
# >> "-------------------------------------------------------------------------"
